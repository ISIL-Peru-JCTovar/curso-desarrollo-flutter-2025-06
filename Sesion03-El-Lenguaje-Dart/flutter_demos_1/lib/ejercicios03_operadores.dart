void main() {
  //-- Operadores aritmeticos
  int suma = 5 + 3;
  print(suma);
  int resta = 15 - 6;
  print(resta);
  double multiplicacion = 14.5 * 20;
  print(multiplicacion);
  double division1 = 25 / 6;
  print(division1);
  int division2 = 25 ~/ 6;
  print(division2);
  int residuo = 25 % 6;
  print(residuo);
  //-- Operadores condicionales  
  print('5 > 3: ${5 > 3}');
  print('5 < 3: ${5 < 3}');
  print('7 == 7: ${7 == 7}');
  print('7 != 7: ${7 != 7}');
  print('14 >= 7: ${14 >= 7}');
  print('14 <= 7: ${14 <= 7}');
}